package Java_Program.basics;

public class print_number {
    public static void main(String[] args){
        System.out.println("4");     // print as text
        System.out.println(4);       // print as value also 4
        System.out.println("4+3+9"); // print text
        System.out.println(4+3+9);   // print value
    }
}
