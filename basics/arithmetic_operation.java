package Java_Program.basics;

public class arithmetic_operation {
    public static void main(String[] args) {

        // Arithmetic operation
        int x=5;
        int y=2;

        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);
    }
}
